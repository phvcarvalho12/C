#include <stdio.h>

main()
{
	float a1, a2;
	a1 = 3.141687;
	a2 = 40.372837;
	
	printf("Soma: %f + %f = %f\n", a1, a2, a1+a2);
	printf("Subtracao: %f - %f = %f\n",a1, a2, a1-a2);
	printf("Multiplicacao: %f * %f = %f\n", a1, a2, a1*a2);
	printf("Divisao: %f / %f = %f\n", a2, a1, a2/a1);
	
}